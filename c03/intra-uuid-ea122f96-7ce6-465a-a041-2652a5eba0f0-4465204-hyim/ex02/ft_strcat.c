char	*ft_strcat(char *dest, char *arc)
{
	char	*cp;

	cp = dest;
	while (*cp)
		cp++;
	while (*arc)
	{
		*cp = *arc;
		cp++;
		arc++;
	}
	*cp = '\0';
	return (dest);
}
