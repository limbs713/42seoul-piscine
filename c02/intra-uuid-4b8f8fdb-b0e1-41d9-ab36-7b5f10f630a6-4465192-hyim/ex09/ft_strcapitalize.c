char	ft_strcapitalize(char *str)
{
	if (*str >= 'a' && *str <= 'z')
	{
		*str = *str - 'a' + 'A';
		str++;
	}
	while (*str)
	{
		if (*str >= 'A' && *str <= 'Z')
		{
			*str = *str - 'A' + 'a';
		}
		str++;
	}
	return (str);
}
